package class1;

public class EscapeSequence {
    public static void main(String[] args) {
        System.out.println("Hellow world");
        System.out.println("Hellow \t world");
        System.out.println("Hellow \n world");
        System.out.println("Hellow \" world\"");
        System.out.println("Hellow \\ world");
    }
}
